// For needs 3 expresions.
// 1 - Initial Expresion = Index or i = 0
// 2 - Condition = loop will execite while condition is true.
// 3 - Increment Expression = i++

for (let i = 1; i <= 5; i++) {
    if (i % 2 !== 0) {
        console.log(i);
    }
}

for (let i = 5; i >= 0; i--) {
    if (i % 2 !== 0) {
        console.log(i);
    }
}
